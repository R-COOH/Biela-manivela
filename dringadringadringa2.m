%dados
rotacao = 2000 * 2 * pi / 60;
r = 0.06; %manivela
l = 0.14; %biela
mb = 0.2; %massa bloco
ma = 1 ;  %massa em a
g=9.81;
t = linspace(0,0.1,10000); %tempo analizado

%angulos

theta = rotacao * t;  %angulo theta

phi = asin(r .* sin(theta)./l);  %angulo phi
%possivelmente algo dando errado com esse angulo

%cinematica
    %angular
    dphi =( (r.*cos(theta)) / (l.*cos(phi) ) ).* rotacao; %velocidade de phi
    ddphi =( l.*dphi.^2 .* sin(phi) - r .* rotacao^2 .* sin(theta) ) ./ (l .* cos(phi)); %aceleração de phi

    %linear

    S = r .*cos(theta) + l.*cos(phi);

    vel= -r .* (sin(theta)+cos(theta).* tan(phi)) .* rotacao; %velocidade do bloco

    ace=-l.*ddphi.*sin(phi) -r*(rotacao^2) .* cos(theta) - l * (dphi^2 .* cos(phi)); %aceleração do bloco
    %nao deu a barriga

%forças
     %força na biela
    Fb = (mb.*ace-mb*g)./cos(phi);
    Yb = Fb .* cos(phi);
    Xb = Fb .* sin(phi);

    %torque
    T = -Xb.*r.*cos(theta) - Yb .* r .* sin(theta);

    %reações
    Ya= -Yb-ma*g ;
    Xa=-Xb;

%forças internas

N = Ya.*cos(theta)+Xa.*sin(theta) - ma.*g.*cos(theta) ./ 2;
V = Ya .* sin(theta) - Xb .* cos(theta) - sin(theta).*ma.*g./2;
M = -T - V.* r/2;

